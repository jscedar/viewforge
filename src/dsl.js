function buildDSL() {
  const pipeline = {
    $pipeline: "viewforge/1.0",
    sources: {},
    steps: [],
    output: null
  };

  state.nodes.forEach(node => {
    if (node.type === 'source') {
      pipeline.sources[node.data.sourceName] = {
        $ref: node.data.sourceName,
        select: node.data.fields ? node.data.fields.map(f => f.name) : "*"
      };
    }
  });

  // topological-ish order via edges
  state.nodes.forEach(node => {
    if (node.type === 'source') return;
    const inputs = state.edges.filter(e => e.to === node.id).map(e => {
      const src = state.nodes.find(n => n.id === e.from);
      return src ? (src.data.sourceName || src.data.label || src.id) : e.from;
    });

    let step = { $op: node.type, id: node.id };
    if (inputs.length) step.$from = inputs.length === 1 ? inputs[0] : inputs;

    if (node.type === 'join') {
      step.type = node.data.joinType || 'inner';
      step.on = node.data.joinKey || '';
    } else if (node.type === 'filter') {
      step.where = node.data.condition || '';
    } else if (node.type === 'map') {
      step.fields = (node.data.fields || []).map(f => f.name);
      if (node.data.mapping) step.compute = node.data.mapping;
    } else if (node.type === 'aggregate') {
      step.fn = node.data.aggFn || 'sum';
      step.field = node.data.aggField || '';
    } else if (node.type === 'groupby') {
      step.by = (node.data.groupFields || '').split(',').map(s=>s.trim()).filter(Boolean);
    } else if (node.type === 'sort') {
      step.by = node.data.sortField || '';
      step.order = node.data.sortOrder || 'asc';
    } else if (node.type === 'output') {
      step.name = node.data.outputName || 'my_view';
      if (node.data.outputSchema) step.shape = node.data.outputSchema;
      pipeline.output = step.name;
    }
    pipeline.steps.push(step);
  });

  // when in builder view, inject builder schema into output step
  if (activeCanvasView === 'builder' && builderTree.length) {
    const name = document.getElementById('builder-output-name')?.value || 'my_view';
    pipeline.output = name;
    const outStep = pipeline.steps.find(s => s.$op === 'output');
    if (outStep) {
      outStep.name = name;
      outStep.shape = builderToSchema(builderTree);
    } else {
      pipeline.steps.push({ $op: 'output', name, shape: builderToSchema(builderTree) });
    }
  }

  return pipeline;
}

function refreshDSL() {
  const dsl = buildDSL();
  const pretty = syntaxHighlight(JSON.stringify(dsl, null, 2));
  document.getElementById('dsl-output').innerHTML = pretty;
  generateCode(state.currentLang);
}

function syntaxHighlight(json) {
  return json
    .replace(/("(\\u[a-zA-Z0-9]{4}|\\[^u]|[^\\"])*"(\s*:)?|\b(true|false|null)\b|-?\d+(?:\.\d*)?(?:[eE][+\-]?\d+)?)/g, match => {
      let cls = 'num';
      if (/^"/.test(match)) {
        if (/:$/.test(match)) cls = 'key';
        else cls = 'str';
      } else if (/true|false/.test(match)) cls = 'kw';
      else if (/null/.test(match)) cls = 'kw';
      return `<span class="${cls}">${match}</span>`;
    });
}

function copyDSL() {
  const dsl = JSON.stringify(buildDSL(), null, 2);
  navigator.clipboard.writeText(dsl).catch(()=>{});
  document.getElementById('status-msg').textContent = 'DSL copied!';
  setTimeout(()=> document.getElementById('status-msg').textContent = '', 2000);
}

function showDSL() {
  const dsl = JSON.stringify(buildDSL(), null, 2);
  document.getElementById('modal-dsl-content').innerHTML = syntaxHighlight(dsl);
  openModal('modal-dsl');
}

